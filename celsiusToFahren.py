# python program to convert temperature in Celsius to Fahrenheit
# change this value for different result
celsiusDegree = 37.5
# calculate fahrenheit
celsiusToFahre = (celsiusDegree * 1.8) + 32
# display the Celsius to Fahrenheit degree
print("%0.1f celsius degree is converted to %0.1f fahrenheit." % (celsiusDegree, celsiusToFahre))

# program to convert temperatre in fahren to Celsius
fahrenTem = 99.5
# calculate celsius
celsiusTem = (fahrenTem - 32) / 1.8
print("%0.1f fahrenheit degree is converted to %0.1f Celsius." % (fahrenTem, celsiusTem))